//
// Face 1:
//   ~ ~
//  0 0
//   o
//   -
//
// Face 2:
//  ~ ~
//  0 0
//   o
//  \_/

#include <stdio.h>

int main(void) {
    printf("~ ~\n");
    printf("0 0\n");
    printf(" o\n");
    printf(" -\n");

    printf("~ ~\n");
    printf("0 0\n");
    printf(" o\n");
    printf("\\_/\n");

    return 0;
}